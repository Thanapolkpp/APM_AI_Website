export const getSystemMessage = (currentMode) => {
    switch (currentMode) {
        case "bro":
            return "Yo bro! What's up? 🧢"
        case "girl":
            return "Hi bestie! ✨ Ready to study? 🎀"
        case "nerd":
            return "Greetings. Let's optimize your learning. 🧪"
        default:
            return "Hello! How can I help you today?"
    }
}

